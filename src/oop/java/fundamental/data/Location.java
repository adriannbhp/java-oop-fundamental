package oop.java.fundamental.data;

/**
 * Abstract Class
 * - Saat kita membuat class, kita bisa menjadikan sebuah class sebagai abstract class
 * - Abstract class artinyam class tersebu tidak bisa dibuat sebgai object secara
 *   langsung, hanya bisa diturunkan
 * - Untuk membuat sebuah class menjadi abstract, kita bisa menggunakan kata kunci abstract
 *   sebelum kata kunci class
 * - Dengan demikian abstract bisa kita gunakan sebgai kontrak untuk class child
 */
public abstract class Location {
    public String name;
}
